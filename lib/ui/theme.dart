import 'package:flutter/material.dart';



TextStyle themestyle(double sz) {
  return new TextStyle(
    fontStyle: FontStyle.italic,
    fontWeight: FontWeight.w600,
    fontSize: sz,
    color: Colors.white,
  );
}

TextStyle themestyle2(double sz) {
  return new TextStyle(
    fontStyle: FontStyle.italic,
    fontWeight: FontWeight.w600,
    fontSize: sz,
    color: Colors.grey.shade700,
  );
}